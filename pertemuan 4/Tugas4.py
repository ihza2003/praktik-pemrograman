a = input("Masukan nilai a =")

b = input("Masukan nilai b =")


hasilTambah = (int(a) + int(b)) #Pertambahan
print("hasil a + b =" + str(hasilTambah))

hasilKurang = (int(a) - int(b)) #Pengurangan
print("hasil a - b =" + str(hasilKurang)) 

hasilKali = (int(a) * int(b)) #Perkalian
print("hasil a * b =" + str(hasilKali))

hasilBagi = (int(a) // int(b)) #Pembagian
print("hasil a // b =" + str(hasilBagi))

hasilSisa = (int(a) % int(b)) #Modulus atau sisa
print("hasil a % b = " + str(hasilSisa))